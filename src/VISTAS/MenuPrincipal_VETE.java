    private void vaciarCampos() {
        jTextIDCliente.setText("");
        jTextDNI.setText("");
        jTextApellido.setText("");
        jTextNombre.setText("");
        jTextDireccion.setText("");
        jTextTelefono.setText("");
        jTextContactoA.setText("");
        jRadioActivo.setSelected(false);
        jRadioInactivo.setSelected(false);
    }

    private void rellenarCampos(Cliente p_cliente) {
        jTextIDCliente.setText(Integer.toString(p_cliente.getId_cliente()));
        jTextDNI.setText(Long.toString(p_cliente.getDni()));
        jTextApellido.setText(p_cliente.getApellido());
        jTextNombre.setText(p_cliente.getNombreD());
        jTextDireccion.setText(p_cliente.getDireccion());
        jTextTelefono.setText(p_cliente.getTelefono());
        jTextContactoA.setText(p_cliente.getContactoA());
        jRadioActivo.setSelected(p_cliente.getActivo());
        jRadioInactivo.setSelected(!p_cliente.getActivo());

    }

    private int opcionABuscar() {
        String[] options = {"ID Cliente", "Apellido", "DNI"};

        int x = JOptionPane.showOptionDialog(null, "Elija el modo de busqueda",
                "Selecciona una opcion",
                JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE, null, options, options[0]);
        return x;
    }

